package com.example.harpochat.data

import androidx.room.*
import kotlinx.coroutines.flow.Flow

@Dao
interface ThreadDao {
    @Query("SELECT * FROM threads ORDER BY title")
    fun threadsFlow(): Flow<List<ThreadEntity>>

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun upsert(thread: ThreadEntity)
}

@Dao
interface MessageDao {
    @Query("SELECT * FROM messages WHERE threadId=:threadId ORDER BY time ASC")
    fun messagesFlow(threadId: String): Flow<List<MessageEntity>>

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insert(m: MessageEntity)

    @Update suspend fun update(m: MessageEntity)

    @Query("UPDATE messages SET status=:status WHERE id=:id")
    suspend fun setStatus(id: String, status: Int)
}
